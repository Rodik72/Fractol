#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int i2;

	i = 0;
	i2 = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[i2] != '\0')
	{
		s1[i] = s2[i2];
		i2++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
